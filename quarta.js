/************* Supino reto com halteres *************/ 
document.getElementById("showGif17").addEventListener("click", function(event) {
    event.preventDefault();
    let gif = document.getElementById("gifContainer17");
    let closeBtn = document.getElementById("closeGif17");
    gif.style.display = "block";
    closeBtn.style.display = "block";
    setTimeout(() => { gif.style.opacity = "1"; }, 10);
});

document.getElementById("closeGif17").addEventListener("click", function() {
    let gif = document.getElementById("gifContainer17");
    let closeBtn = document.getElementById("closeGif17");
    gif.style.opacity = "0";
    setTimeout(() => {
        gif.style.display = "none";
        closeBtn.style.display = "none";
    }, 500);
});

