/********************** Cadeira extensora ***********************/ 
document.getElementById("showGif33").addEventListener("click", function(event) {
    event.preventDefault();
    let gif = document.getElementById("gifContainer33");
    let closeBtn = document.getElementById("closeGif33");
    gif.style.display = "block";
    closeBtn.style.display = "block";
    setTimeout(() => { gif.style.opacity = "1"; }, 10);
});

document.getElementById("closeGif33").addEventListener("click", function() {
    let gif = document.getElementById("gifContainer33");
    let closeBtn = document.getElementById("closeGif33");
    gif.style.opacity = "0";
    setTimeout(() => {
        gif.style.display = "none";
        closeBtn.style.display = "none";
    }, 500);
});

