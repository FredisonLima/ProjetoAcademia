/************* Supino reto com halteres deitado *************/ 
document.getElementById("showGif").addEventListener("click", function(event) {
    event.preventDefault();
    let gif = document.getElementById("gifContainer");
    let closeBtn = document.getElementById("closeGif");
    gif.style.display = "block";
    closeBtn.style.display = "block";
    setTimeout(() => { gif.style.opacity = "1"; }, 10);
});

document.getElementById("closeGif").addEventListener("click", function() {
    let gif = document.getElementById("gifContainer");
    let closeBtn = document.getElementById("closeGif");
    gif.style.opacity = "0";
    setTimeout(() => {
        gif.style.display = "none";
        closeBtn.style.display = "none";
    }, 500);
});

