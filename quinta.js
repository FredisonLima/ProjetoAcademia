/********************** Puxada alta na polia ***********************/ 
document.getElementById("showGif25").addEventListener("click", function(event) {
    event.preventDefault();
    let gif = document.getElementById("gifContainer25");
    let closeBtn = document.getElementById("closeGif25");
    gif.style.display = "block";
    closeBtn.style.display = "block";
    setTimeout(() => { gif.style.opacity = "1"; }, 10);
});

document.getElementById("closeGif25").addEventListener("click", function() {
    let gif = document.getElementById("gifContainer25");
    let closeBtn = document.getElementById("closeGif25");
    gif.style.opacity = "0";
    setTimeout(() => {
        gif.style.display = "none";
        closeBtn.style.display = "none";
    }, 500);
});

